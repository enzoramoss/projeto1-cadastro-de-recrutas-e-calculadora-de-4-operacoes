let num1 = parseInt(window.prompt("Digite um número: "))
let num2 = parseInt(window.prompt("Digite um outro número: "))
let soma = num1 + num2
let subtracao = num1 - num2
let multiplicacao = num1 * num2
let divisao = num1 / num2

window.alert(`Exemplos de números: ${num1} e ${num2}
Resultado:
${num1} + ${num2} = ${soma}
${num1} - ${num2} = ${subtracao}
${num1} * ${num2} = ${multiplicacao}
${num1} / ${num2} = ${divisao}
`)